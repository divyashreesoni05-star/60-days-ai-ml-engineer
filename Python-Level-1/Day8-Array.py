# Day 8 – Learning Arrays in Python

# Today I learned about arrays in Python.
# Arrays (lists in Python) are used to store multiple values in a single variable.


# Example of an Array
arr = [10, 20, 30, 40, 50]

print("Array:", arr)


# Accessing Array Elements using index
print("First element:", arr[0])
print("Third element:", arr[2])


# Using a for loop to print all elements
print("Elements using loop:")
for i in arr:
    print(i)


# Finding the length of the array
print("Length of array:", len(arr))


# Adding an element to the array
arr.append(60)
print("After adding element:", arr)


# Removing an element from the array
arr.remove(20)
print("After removing element:", arr)


